# FORMATAÇÃO BASICA DE STRINGS

# s- string
# d - int
# f - float
# .<numero de digitos>f
# x ou X - hexadecimal
# (caractere)(><^)(quantidade)
# > - esquerda
# < - direita
# ^ - centro
# sinal - + ou -
# ex.: 0>-100, 1.f
# conversion flags - !r !s !a

variavel = 'ABC'
print(f'{variavel}')
print(f'{variavel: >10}') # nesse exemplo ele jogou 10 caracteres a esquerda