
# inteiro, nomes inteiros, se tiver separação com virgula separa os numeros
print(12345)

# Aspas simples, exemplo, nomes
print('Karlison Matos')

# Aspas duplas, exemplo, nomes
print("Karlison Matos")

# Escape
print("Karlison \"Matos\"") # as barras invertidas com aspas fazem o texto entrar em "aspas"

# r
print(r"Karlison \"Matos\"") # usar o r faz ele exibir o escape


print('Karlison "Matos"') # Dessa maneira com aspas simples e duplas evita ter que usar o escape e deixo o código mais limpo

